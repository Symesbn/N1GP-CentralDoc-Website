	<p>This alternative take on Modcards allows for only cards based on navis to be used. Only 2 modcards can be equipped, but there is no enforced MB limit.<br>
		This format leads to some unique strategies that you wouldn't see elsewhere. This was conceived by the user Nugget.</p>
		
	<p>EXE6 Seniors</p>
	<p>This is the Japanese format in which Modcards are prohibited. All the Japanese-exclusive chips are allowed. This format is rarely played in the N1GP.</p>
	
	<p>BN6 Link Navis</p>
	<img src="Images/FAQ/NEEDFORLINK_s.png">
	<p>This format uses cheatcodes to play as different navis from BN6. These navis have innate abilities, but lack a navicust.<br>
		Playing as Megaman is not allowed in this format.</p>
		
	<p>BN6 Eurandom</p>
	<img src="Images/FAQ/Eurandom2020_s.png">
	<p>This format simulates Random Battles, but uses the TripleBattle mode so there's less downtime between matches. The tournament organizer uses RNG to simulate how the game creates a random folder,<br>

	<h2 id="BN6Q1">Q: What are the rules for BN6?</h2>
</div>

<div class="contentBingus" style="height:500px">	
	<p>Battle Network 6 is a well balanced game and as a result there are very few rules to observe. These rules relate more to sportsmanship and cheat prevention than to the game itself.</p>
	<p>There are various formats which use unique rules, but the following rules will always be true regardless of the format, unless a format's rules explicitly state otherwise.<br>
		Here are the universal rules:</p>
	<p>1) Chips unabtainable without hacking are strictly banned. This applies to giga chips from the opposite version of the one you choose to play. For BN6, this also applies to these Japanese-exclusive chips that were removed from the game:</p>
	<img src="Images/FAQ/018.png">
	<img src="Images/FAQ/153.png">
	<img src="Images/FAQ/275.png">
	<img src="Images/FAQ/278.png">
	<img src="Images/FAQ/311.png">
	<img src="Images/FAQ/312.png">
	<img src="Images/FAQ/313.png">
	<p>(A pvp-ready save file is available for both versions of BN6 and EXE6. These save files do not contain any infringing chips.)</p>
	<p>2) Using cheats to change any values in the game is banned. Using any features resembling auto-fire or turbo controls is not permitted.</p>
	<p>The "Disable Music" gameshark code is allowed. This is the only exception outside of special formats that explicitly allow or require the use of specific cheatcodes.</p>
	<p>3) While playing online, sometimes the game can desync and invalidate the match. Tournament games that ended in a desync are to be replayed. Purposely disconnecting or causing a desync in an attempt to invalidate a match is strictly prohibited.</p>
	<p>4) At the request of either player, the host of a match will be determined by a digital coin flip. If a technical problem prevents a player from being able to host, it is requested that their situation be respected.</p>
</div>


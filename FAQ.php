<div class="questionBingus" style="height:100px">
	<h2>Q: I downloaded one of the save files but it's not working. Or the save works but I can't connect to anyone else ingame. What's wrong?</h2>
</div>

<div class="contentBingus" style="height:420px">	
	<p>For most Battle Network games there are 3 localized versions: US, EU, & JP. Save files and pvp connections are not compatible between different localized versions.</p>
	<p>If you can't load the save file, it's likely because you have the wrong version of the game. If you can't connect to anyone else, it's likely for the same reason.</p>
	<p>Make sure you're not trying to load a Japanese save file in the English version, or vice versa. If you're sure both the save file and game are the English version, the problem may be that you're using the EU game.<br>
		The EU version cannot load our saves or connect to the US version. You want to use the US version.</p>
	<p>How to know which regional version you have:</p>
	<p>For BN6, check the title screen of the game to see which version it is. The US version will have an “&#x24C7” symbol right after "BATTLE NETWORK", while the EU version will have a “™” symbol.<br>
		If you have the EU version, you will need to use the ROM from the proper cart. If you only have access to the EU cart, then you can convert your ROM to the US version with the help of a patch.</p>
	<p>The "BN6 EU to US" Patch is available in the Resource Folder and is included in the main download. Follow the directions in the readme file for specific instructions on how to use it.</p>
	<p>If you successfully loaded a save file but are still failing to connect to anyone else, make sure you did not accidentally use EXE6 while connecting to someone who is playing BN6. The US version cannot connect with the JP version.<br>
		The translation patch for the JP version does not affect the connection compatibility. If you're certain you're using the correct version, then the connection problem is being caused by a different issue.</p>
</div>


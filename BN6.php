<h2 style="color:red">What is "Advanced"?</h2>

<img src="Images/netbattle ex comic.png">

<p>If you're reading the Advanced Guides, we will be assuming you're familiar with Mega Man Battle Network 6 and have played at least some PvP NetBattling.<br>
	If you're not already familiar with the basics of NetBattling, no worries! Check out the NetBattling 101 guide first, then check out the rest of the guides when you want to learn more.<br>
	These advanced guides will go into greater detail about important mechanics, chips, and various interactions. So, hold on to your Mett hats!</p>
	
<p>Some information was taken from the awesome guys over at:<br>

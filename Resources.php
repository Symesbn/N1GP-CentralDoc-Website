<h1>Playerbase Channels</h1>
<p>These are channels from our active playerbase that mainly post videos of pvp netbattles. They're a good source of tournament vods and cool netbattle moments.</p>
<p>Check the tags to see what type of content each channel has. The tags are as follows:</p>
<ul>
	<li>#vods - full matches, usually minimally edited</li>
	<li>#clips - curated moments, edited matches, highlight reels</li>
	<li>#stream - matches streamed on youtube</li>
	<li>#archive - channels no longer active, but filled with videos</li>
</ul>

